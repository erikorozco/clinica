    //Agrega un nuevo reporte a la base de datos o muestra un mensaje de error al fallar la operación
    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        try {                                        
            if((Integer)id_expediente.getValue() < 1){
                JOptionPane.showMessageDialog(null, "Revise el valor del expediente.", "Error de datos.", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(((String)tser.getSelectedItem()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Especifique el tipo de servicio ofrecido.");
                return;
            }
            if(!accesoReporte.expedienteAsignado(accesoReporte.obtenerIdentificadorDeTerapeuta(InicioSesion.identificadorUsuario), (Integer)this.id_expediente.getValue())){
                JOptionPane.showMessageDialog(null, "No se puede obtener acceso a ese paciente.");
                return;
            }
            try {
                Reporte aGuardar = guardarReporte();
                accesoReporte.agregarReporte(aGuardar);
                JOptionPane.showMessageDialog(this, "Reporte #" + aGuardar.getNumSesion() +" del paciente guardado correctamente.");
                this.dispose();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(VentanaReporte.class.getName()).log(Level.SEVERE, null, ex);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar el reporte, favor de revisar los campos.");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VentanaReporte.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_guardarActionPerformed

