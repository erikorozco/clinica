    //Agrega un nuevo reporte a la base de datos o muestra un mensaje de error al fallar la operación
    private Reporte guardarReporte() throws ClassNotFoundException {
        DateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date fecha = new java.util.Date();
        Reporte reporte = new Reporte();
        try { 
            AccesoReporte acceso = new AccesoReporte();
            //reporte.setIdentificadorPersona(Integer.valueOf(this.id_expediente.getText()));
            reporte.ingresarNumExpediente((Integer)id_expediente.getValue());
            reporte.ingresarNumSesion(acceso.siguienteSesion(reporte.getNumExpediente()));
            reporte.ingresarTipoDeServicio(((String)this.tser.getSelectedItem()).trim().toUpperCase());
            reporte.ingresarCita(java.sql.Date.valueOf(formatoFecha.format(fecha)));
            reporte.ingresarApertura(this.ap.getText());
            reporte.ingresarDesarrollo(this.des.getText());
            reporte.ingresarAcuerdos(this.acuerdos.getText());
            reporte.ingresarAvances(this.avances.getText());
            reporte.ingresarProximaCita(java.sql.Date.valueOf(formatoFecha.format(fecha)));
        } catch(Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ingresa únicamente números en los campos numero expediente, numero sesion y id expediente");
        }
        return reporte;
    }
    
